// Create pincode to RTO mapping for quick lookup
export const PINCODE_TO_RTO_MAP: Record<string, RTOOfficeData> = {};
RTO_OFFICES_DATA.forEach((rto) => {
  rto.servesPincodes.forEach((pincode) => {
    PINCODE_TO_RTO_MAP[pincode] = rto;
  });
});

// Get RTO office by pincode
export function getRTOByPincode(pincode: string): RTOOfficeData | null {
  return PINCODE_TO_RTO_MAP[pincode] || null;
}

// Search RTO offices by partial pincode
export function searchRTOsByPincode(partialPincode: string): RTOOfficeData[] {
  if (!partialPincode) return RTO_OFFICES_DATA;

  const matchingRTOs = new Set<RTOOfficeData>();

  Object.keys(PINCODE_TO_RTO_MAP).forEach((pincode) => {
    if (pincode.startsWith(partialPincode)) {
      matchingRTOs.add(PINCODE_TO_RTO_MAP[pincode]);
    }
  });

  return Array.from(matchingRTOs).sort((a, b) => a.name.localeCompare(b.name));
}

// Search RTO offices by RTO code (MH01, MH43, etc.)
export function searchRTOsByCode(searchTerm: string): RTOOfficeData[] {
  if (!searchTerm) return RTO_OFFICES_DATA;

  const upperSearchTerm = searchTerm.toUpperCase();

  return RTO_OFFICES_DATA.filter(
    (rto) =>
      rto.code.toUpperCase().includes(upperSearchTerm) ||
      rto.name.toUpperCase().includes(upperSearchTerm) ||
      rto.city.toUpperCase().includes(upperSearchTerm)
  ).sort((a, b) => a.code.localeCompare(b.code));
}

// Combined search function for both pincode and RTO code
export function searchRTOs(searchTerm: string): RTOOfficeData[] {
  if (!searchTerm) return RTO_OFFICES_DATA;

  // If search term looks like pincode (starts with number)
  if (/^\d/.test(searchTerm)) {
    return searchRTOsByPincode(searchTerm);
  }

  // If search term looks like RTO code or contains MH
  if (/^MH\d*$/i.test(searchTerm) || searchTerm.toUpperCase().includes('MH')) {
    return searchRTOsByCode(searchTerm);
  }

  // General search across all fields
  return searchRTOsByCode(searchTerm);
}

// Get all RTO offices (for fallback)
export function getAllRTOOffices(): RTOOfficeData[] {
  return RTO_OFFICES_DATA.sort((a, b) => a.code.localeCompare(b.code));
}

// Get RTO offices by city
export function getRTOsByCity(city: string): RTOOfficeData[] {
  return RTO_OFFICES_DATA.filter((rto) => rto.city.toLowerCase() === city.toLowerCase()).sort(
    (a, b) => a.code.localeCompare(b.code)
  );
}

// Simple array of RTO office names for backward compatibility
export const RTO_OFFICES = RTO_OFFICES_DATA.map((rto) => rto.name);
